/**
 * Check if a drug has an offline profile available
 */
export function hasOfflineProfile(drugName) {
  return Object.prototype.hasOwnProperty.call(DRUG_PROFILES, drugName);
}

/**
 * Get offline profile for a drug, or null if not available
 */
export function getOfflineProfile(drugName) {
  return DRUG_PROFILES[drugName] || null;
}
